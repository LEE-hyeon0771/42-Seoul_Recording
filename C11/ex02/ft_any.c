int	ft_any(char **tab, int (*f)(char*))
{
	int	answer;
	int	i;

	i = 0;
	while (tab[i] != 0)
	{
		answer = f(tab[i]);
		if (answer != 0)
		{
			return (1);
		}
		i++;
	}
	return (0);
}
